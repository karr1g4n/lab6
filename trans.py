import numpy as np
a=3#розмірність
b=3#різмірність
mas=[]#матрица
for i in range(a):
    d=[]#доб. масив
    for j in range(b):
        d.append(int(input("введите первую матрицу по елементно:")))#перебор
    mas.append(d)#добавленя елементов с доб. матрици в основную
a = np.array(mas)#надання вигляду матрици 
a = a.transpose()#транспонування
print(a)